int P,T;
namespace Subtask1{
	const char ch[]="GRBG";
	char S[M];
	int n,mark[M];
	void solve(){
		n=strlen(S+1);
		int mx=0,mi=0;
		for(int i=1;i<=n;i++){
			if(S[i]=='(')mx+=2,mi+=1;
			else {
				mi=max(mi-2,0);mx--;
				if(mx<0){puts("impossible");return;}	
			}
		}
		if(mi>0){puts("impossible");return;}
		for(int i=1;i<=n;i++)mark[i]=0;
		for(int i=n,clo=1;i&&mx;i--,mx--)
			if(S[i]=='(')mark[i]=clo,clo=3-clo;
			else mark[i]=3;
		for(int i=1,clo=1;i<=n;i++)
			if(S[i]==')'&&mark[i]<3)mark[i]=clo,clo=3-clo;
		for(int i=1;i<=n;i++)
			putchar(ch[mark[i]]);
		puts("");
	}
	void run(){
		for(int i=1;i<=T;i++){
			scanf("%s",S+1);
			solve();	
		}
	}
}
